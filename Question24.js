//Equality with strings
console.log('strawberry' == 'strawberry'); // True
//using the lowercase function
console.log('strawberry'.toLowerCase() == 'strawberry'); // True
//Equality with numbers
console.log(1 == 1); // True
//numerical test
console.log(1 > 2); // False    
console.log(1 < 2); // True
//Condition && and or
console.log(1 == 1 && 2 == 2); // True
console.log(1 == 1 || 2 == 2); // True
/// Test whether an item is in a array
var fruit = ["apple", "banana", "cherry"];
console.log("Is 'apple' in fruits?");
console.log(fruit.includes("apple")); // True
console.log("Is 'strawberry' in fruits?");
console.log(fruit.includes("strawberry")); // false
